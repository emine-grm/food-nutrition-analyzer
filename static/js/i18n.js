const NouraI18n = (function(){
  function getLang(){
    try{ return localStorage.getItem('noura_lang') || 'en'; }catch(e){ return 'en'; }
  }
  function setLang(lang){
    try{ localStorage.setItem('noura_lang', lang); }catch(e){}
  }
  function t(key){
    const lang = getLang();
    return (DICT[lang] && DICT[lang][key]) || DICT.en[key] || key;
  }
  function apply(){
    const lang = getLang();
    const d = DICT[lang] || DICT.en;
    document.documentElement.lang = lang;
    document.documentElement.dir = d.dir;
    document.querySelectorAll('[data-i18n]').forEach(function(el){
      const key = el.getAttribute('data-i18n');
      if(d[key] !== undefined) el.textContent = d[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el){
      const key = el.getAttribute('data-i18n-placeholder');
      if(d[key] !== undefined) el.setAttribute('placeholder', d[key]);
    });
    document.querySelectorAll('.lang-switch button').forEach(function(btn){
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
  }
  function init(){
    apply();
    document.querySelectorAll('.lang-switch button').forEach(function(btn){
      btn.addEventListener('click', function(){
        setLang(btn.dataset.lang);
        apply();
        window.dispatchEvent(new CustomEvent('noura:langchange'));
      });
    });
  }
  return { getLang, setLang, t, apply, init };
})();

document.addEventListener('DOMContentLoaded', NouraI18n.init);
